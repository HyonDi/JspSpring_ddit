package com.bms.dto.suggestion;

import java.util.Date;

public class SuggestionVO {
	
	private String suggestion_code;
	private String member_code;
	private String suggestion_title;
	private String suggestion_contents;
	private int suggestion_view_cnt;
	private Date suggestion_reg_date;
	private Date suggestion_update_date;
	private String board_sort_code;
	
	public String getSuggestion_code() {
		return suggestion_code;
	}
	public void setSuggestion_code(String suggestion_code) {
		this.suggestion_code = suggestion_code;
	}
	public String getMember_code() {
		return member_code;
	}
	public void setMember_code(String member_code) {
		this.member_code = member_code;
	}
	public String getSuggestion_title() {
		return suggestion_title;
	}
	public void setSuggestion_title(String suggestion_title) {
		this.suggestion_title = suggestion_title;
	}
	public String getSuggestion_contents() {
		return suggestion_contents;
	}
	public void setSuggestion_contents(String suggestion_contents) {
		this.suggestion_contents = suggestion_contents;
	}
	public int getSuggestion_view_cnt() {
		return suggestion_view_cnt;
	}
	public void setSuggestion_view_cnt(int suggestion_view_cnt) {
		this.suggestion_view_cnt = suggestion_view_cnt;
	}
	public Date getSuggestion_reg_date() {
		return suggestion_reg_date;
	}
	public void setSuggestion_reg_date(Date suggestion_reg_date) {
		this.suggestion_reg_date = suggestion_reg_date;
	}
	public Date getSuggestion_update_date() {
		return suggestion_update_date;
	}
	public void setSuggestion_update_date(Date suggestion_update_date) {
		this.suggestion_update_date = suggestion_update_date;
	}
	public String getBoard_sort_code() {
		return board_sort_code;
	}
	public void setBoard_sort_code(String board_sort_code) {
		this.board_sort_code = board_sort_code;
	}
	
	@Override
	public String toString() {
		return "SuggestionVO [suggestion_code=" + suggestion_code + ", member_code=" + member_code
				+ ", suggestion_title=" + suggestion_title + ", suggestion_contents=" + suggestion_contents
				+ ", suggestion_view_cnt=" + suggestion_view_cnt + ", suggestion_reg_date=" + suggestion_reg_date
				+ ", suggestion_update_date=" + suggestion_update_date + ", board_sort_code=" + board_sort_code + "]";
	}
	

}
